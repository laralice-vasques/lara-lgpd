export function AgentsOfDataTreatment() {
  return (
    <>
      De acordo com a LGPD, existem dois principais agentes para o tratamento de
      dados pessoais: o controlador e o operador. O controlador é quem toma as
      decisões em relação aos dados pessoais, é a empresa/organização que define
      como os dados serão coletados, para quais finalidades e por quanto tempo
      serão mantidos armazenados. O operador não toma decisões, é quem processa
      os dados sob as ordens do controlador.
    </>
  );
}
