export function Finality() {
  return (
    <>
      O princípio Finalidade diz que só é permitido trabalhar com os dados do
      cliente se for para propósitos legítimos, específicos, explícitos e
      informados ao titular, sem possibilidade de tratamento posterior de forma
      incompatível com essas finalidades. Ou seja, o propósito do uso do dado
      pessoal do titular deve estar explícito, a finalidade da coleta daquele
      dado deve ser clara e ser exposta de forma verdadeira.
      <br />
      Por exemplo, quando um cliente está fazendo uma compra online e fornece
      seu e-mail no ato da compra, e na sequência recebe uma série de e-mails
      com ofertas de outros produtos, esse princípio está sendo ferido no caso
      desse cliente não ter sido informado que receberia esses e-mails. Outro
      exemplo de como esse princípio pode ser ferido, é a solicitação do CPF do
      cliente para elaborar uma proposta sem deixar claro que esse CPF será
      consultado em instituições de crédito.
      <br />
      Além disso, ao pedir o consentimento do titular para o tratamento dos
      dados, o titular deve saber o que está consentindo, ou seja, deve estar
      ciente sobre a finalidade do tratamento dos dados.
      <br />
      Caso a finalidade não esteja explícita ao titular, esse princípio está
      sendo ferido. A empresa, nesse caso, precisa reavaliar a coleta de dados e
      informar ao titular o que fará com os dados coletados, e que esse objetivo
      seja verdadeiro e adequado, onde entra o princípio da adequação.
      <br />
      A empresa também deve verificar se é utilizada alguma salvaguarda para
      justificar a transferência internacional de dados pessoais, já que o
      titular deve fornecer o seu consentimento específico e em destaque para a
      transferência.
      <br />
      Art. 33. da LGPD “A transferência internacional de dados pessoais somente
      é permitida nos seguintes casos:
      <br />
      I - para países ou organismos internacionais que proporcionem grau de
      proteção de dados pessoais adequado ao previsto nesta Lei;
      <br />
      II - quando o controlador oferecer e comprovar garantias de cumprimento
      dos princípios, dos direitos do titular e do regime de proteção de dados
      previstos nesta Lei, na forma de:
      <br />
      a) cláusulas contratuais específicas para determinada transferência;
      <br />
      b) cláusulas-padrão contratuais;
      <br />
      c) normas corporativas globais;
      <br />
      d) selos, certificados e códigos de conduta regularmente emitidos;
      <br />
      III - quando a transferência for necessária para a cooperação jurídica
      internacional entre órgãos públicos de inteligência, de investigação e de
      persecução, de acordo com os instrumentos de direito internacional;
      <br />
      IV - quando a transferência for necessária para a proteção da vida ou da
      incolumidade física do titular ou de terceiro; <br />
      V - quando a autoridade nacional autorizar a transferência;
      <br />
      VI - quando a transferência resultar em compromisso assumido em acordo de
      cooperação internacional;
      <br />
      VII - quando a transferência for necessária para a execução de política
      pública ou atribuição legal do serviço público, sendo dada publicidade nos
      termos do inciso I do caput do art. 23 desta Lei;
      <br />
      VIII - quando o titular tiver fornecido o seu consentimento específico e
      em destaque para a transferência, com informação prévia sobre o caráter
      internacional da operação, distinguindo claramente esta de outras
      finalidades;
      <br />
      IX - ou quando necessário para atender as hipóteses previstas nos incisos
      II, V e VI do art. 7º desta Lei.
    </>
  );
}
