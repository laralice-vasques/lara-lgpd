export function Necessity() {
  return (
    <>
      O princípio da Necessidade diz que os dados só devem ser pedidos se forem
      necessários. Se a empresa for elaborar uma proposta para um cliente, por
      exemplo, ela não precisa pedir fotocópia de documentos, nome da mãe e nome
      dos filhos, porque esses dados não são necessários. Além disso, mesmo que
      os dados sejam pedidos por algum motivo específico, eles não devem ser
      armazenados por tempo indeterminado e sim pelo período que seja
      necessário. <br />
      Se a empresa não estiver de acordo com o princípio da necessidade, deve-se
      rever as operações de tratamento de dados e adequá-las para que os dados
      coletados sejam somente aqueles dados realmente necessários para a
      operação. Além disso, não guardar por mais tempo que o necessário, depois
      que a finalidade do tratamento seja cumprida.
    </>
  );
}
